public class Animal {
  private int age;
  private String name;

  public Animal(int age) {
    super();
    this.age = age;
  }
  public Animal(int age) {
    super();
    this.age = age;
    this.name = null;
  }
  public void setAge(int age) {
    this.age = age;
  }
  public int getAge() {
    return age;
  }
}
